function telephoneCheck(str) {
  let f = [/^([1]\s|)\d{3}-\d{3}-\d{4}$/,
           /^([1]|)[(]\d{3}[)]\d{3}[-]\d{4}$/,
           /^([1]\s|)[(]\d{3}[)]\s\d{3}[-]\d{4}$/,
           /^([1]\s|)\d{3}\s\d{3}\s\d{4}$/,
           /^\d{10}$/,
           ];
  for (let i in f) if (f[i].test(str)) return true;
  return false;
}
console.log(telephoneCheck("1 555-555-5555"));
// 1 555-555-5555
// 1 (555) 555-5555
// 1(555)555-5555

// 5555555555
